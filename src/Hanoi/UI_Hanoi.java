    public void contador(int NumAros) {
        //String seleccionado = (String) NunAros.getSelectedItem();
        ContadorMov++;
        TXT_MovHechos.setText(ContadorMov + "");
        if (ContadorMov > Integer.parseInt(N_Mov.getText())) {
            TXT_MovHechos.setForeground(Color.red);
        }
        if ((ContadorMov == Integer.parseInt(N_Mov.getText())) && (NumAros == (Panel_TorreC.getComponentCount()))) {
            JOptionPane.showMessageDialog(null, "Felicidades");
        }
    }

    private ArrayList moverAros(JPanel Pasa, JPanel Recibe, int y_Recibe) {
        ArrayList<Component> panels = new ArrayList();
        int arosA, arosB;
        try {
            if (Recibe.getComponentCount() < 1) {
                Component[] labels = Pasa.getComponents();
                arosA = labels.length - 1;
                labels[arosA].setBounds(labels[arosA].getX(), y_Recibe, labels[arosA].getWidth(), labels[arosA].getHeight());
                Recibe.add(labels[arosA]);
                panels.add(Pasa);
                panels.add(Recibe);
                contador(Seleccionado);
                y = true;
            } else {
                Component[] labelsA = Pasa.getComponents();
                Component[] labelsB = Recibe.getComponents();
                arosA = labelsA.length - 1;
                arosB = labelsB.length - 1;
                if (labelsB[arosB].getWidth() < labelsA[arosA].getWidth()) {
                    JOptionPane.showMessageDialog(null, "No puede colocar una anillo mas grande sobre uno mas pequeño");
                    panels.add(Pasa);
                    panels.add(Recibe);
                    y = false;
                } else {
                    labelsA[arosA].setBounds(labelsA[arosA].getX(), y_Recibe, labelsA[arosA].getWidth(), labelsA[arosA].getHeight());
                    Recibe.add(labelsA[arosA]);
                    panels.add(Pasa);
                    panels.add(Recibe);
                    contador(Seleccionado);
                    y = true;
                }
            }

        } catch (Exception e) {

        }

        return panels;
    }
