    private void Iniciar() {
        this.Panel_TorreA.removeAll();
        this.Panel_TorreB.removeAll();
        this.Panel_TorreC.removeAll();
        ContadorMov = 0;
        y_ArosA = 150;
        y_ArosB = 150;
        y_ArosC = 150;
        y = false;
        TXT_MovHechos.setForeground(Color.white);
        String seleccionado = (String) NunAros.getSelectedItem();
        Seleccionado = Integer.parseInt(seleccionado);
        CrearAros(Seleccionado);
    }

    private void CrearAros(int NumAros) {
        int w = 130;
        int x = 10;
        //Aros Aro;
        //int randomNum;
        for (int contador = 0; contador < NumAros; contador++) {
            //randomNum = ThreadLocalRandom.current().nextInt(0, 5) + 1;
            JLabel Aros = new JLabel();
            Aros.setBounds(x, y_ArosA, w, 15);
            Aros.setVerticalAlignment(JLabel.CENTER);
            Aros.setVisible(true);

            Image img = new ImageIcon("src/Imagenes/Aro.png").getImage();
            ImageIcon img2 = new ImageIcon(img.getScaledInstance(w, 15, Image.SCALE_SMOOTH));
            Aros.setIcon(img2);

            //Aro = new Aros(x,w,y_ArosA);
            this.Panel_TorreA.add(Aros);

            Panel_TorreA.updateUI();
            Panel_TorreB.updateUI();
            Panel_TorreC.updateUI();
            y_ArosA = y_ArosA - 15;
            w = w - 10;
            x = x + 5;
        }
        int m;
        m = (int) (Math.pow(2, NumAros) - 1);
        N_Mov.setText(m + "");
        TXT_MovHechos.setText(ContadorMov + "");
    }

